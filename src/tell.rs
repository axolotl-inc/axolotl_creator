pub fn intro() {
    println!("Welcome to axolotl creator!");
    println!("A place where you can create ANY axolotl you want!");
}

pub fn help() {
    println!("Commands:");
    println!("\tadd, adds an axolotl to your collection.");
    println!("\thelp, print help menu.");
    println!("\tlist, list your beautiful axolotls.");
    println!("\tedit, change one of your axolotls.");
    println!("\tkill, please don't do it.");
    println!("\tload, read a save file.");
    println!("\tsave, save your collection of axolotls to a file.");
    println!("\tstop, close program.");
}